#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (s)
	{
		while (s[i])
		{
			if (write(fd, &s[i], 1) < 0)
			{
				perror("write");
				return ;
			}
			i++;
		}
	}
}
